public class StackExample {
    // Function to calculate the sum of two numbers
    public static int sum(int a, int b) {
        int result = a + b; 
        return result; 
    }

    public static void main(String[] args) {
        int x = 5; 
        int y = 10; 
        int total = sum(x, y); 
        System.out.println("Total is: " + total); 
    }
}
